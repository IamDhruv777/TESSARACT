@app.post("/api/webhook")
async def receive_webhook(payload: WebhookPayload, background_tasks: BackgroundTasks):
    campaign_id = str(uuid.uuid4())
    
    # Initialize campaign
    steps = [{"name": step["name"], "status": "pending"} for step in PIPELINE_STEPS]
    steps[0]["status"] = "completed" # Webhook trigger is instant
    
    campaigns_db[campaign_id] = {
        "id": campaign_id,
        "feature_name": payload.feature_name,
        "description": payload.description,
        "status": "processing",
        "current_step_index": 1,
        "steps": steps,
        "result": None
    }
    
    background_tasks.add_task(process_pipeline, campaign_id, payload.feature_name, payload.description)
    return {"message": "Webhook received, processing started", "campaign_id": campaign_id}

@app.get("/api/campaigns")
async def get_campaigns():
    # Return as list, newest first
    return {"campaigns": list(campaigns_db.values())[::-1]}

@app.get("/api/campaigns/{campaign_id}")
async def get_campaign(campaign_id: str):
    if campaign_id in campaigns_db:
        return campaigns_db[campaign_id]
    return {"error": "Campaign not found"}, 404

